<h1>About me</h1>

<p>Hi,</p>
<p>I'm Carolina Doran.</p>
<p>I'm a scientific researcher working in the field of collective animal behaviour.</p>
<p>I believe it is a scientists duty to let the world know about their research - and, just as importantly, to hear what the world thinks about their research.</p>
<p>I also think that everyone has the right to persue their interest in science and endeavour to support anyone who has less access and opportunity so that we can all be part of understanding this world.</p>
<p>This is why I run Soapbox Science in Berlin. It is an initiative to promote women in science and the work that they do.</p>